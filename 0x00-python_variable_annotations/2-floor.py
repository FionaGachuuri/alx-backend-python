#!/usr/bin/env python3
import math

"""
This script defines a function to compute the floor of a number.
"""


def floor(n: float) -> int:
    """
    Computes the floor of a number.

    Args:
        n (Union[int, float]): The number to compute the floor of.

    Returns:
        int: The floor of the number.
    """
    return math.floor(n)
